#include <stdio.h>

int main(){
    int i = 10, j = 5;
    float x = 5.0, y;
    
    // 程序C：只执行强制类型转换的除法
    // y = x / i; 
    // y = j / i;     
    y = (float)j / i;
    
    printf("y = %f\n", y);
    
    return 0;
}

/* 
三个程序的结果分析：

程序A: y = x / i; 
结果: y = 0.500000
原因: x是float类型(5.0)，i是int类型(10)，5.0/10=0.5

程序B: y = j / i;
结果: y = 0.000000  
原因: j和i都是int类型，5/10=0（整数除法），然后赋值给float类型y

程序C: y = (float)j / i;
结果: y = 0.500000
原因: j被强制转换为float类型(5.0)，5.0/10=0.5

结论：
- 当两个操作数都是整数时，进行整数除法，结果截断小数部分
- 当至少一个操作数是浮点数时，进行浮点数除法，保留小数部分
- 强制类型转换可以改变运算的数据类型，从而影响结果
*/